// | `Quero somar` | Faça um programa que peça dois números e imprima no console a soma desses dois números.|

var n1 = prompt("Digite o primeiro número: ");
var n2 = prompt("Digite o segundo número: ");

var n3 = parseInt(n1)+ parseInt(n2);

console.log(`A soma de ${n1} e ${n2} é ${n3}.`);